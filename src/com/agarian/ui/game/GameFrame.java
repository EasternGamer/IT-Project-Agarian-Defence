            // TODO add your handling code here:
            new MenuGUI().setVisible(true);
            DatabaseUtils db = new DatabaseUtils();
            db.save(Main.username, money, level, PANEL_GRID.hqHealth, GamePanel.creatures, GamePanel.turrets).end();
