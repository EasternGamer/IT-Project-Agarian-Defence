    @Override
    public void actionPerformed(ActionEvent ae) {
        jLabelTimeRemaining.setText("Time remaining: " + time);
        time -= 100;
        if (time == 0) {
            time = 25000;
            level++;
            jLabelTimeRemaining.setText("");
            spacedTimer = new SpacedTimer(PANEL_GRID, level + 1);
            spacedTimer.startTimer();
            jLabelLevel.setText(level + "");
            timer.stop();
        }
    }
